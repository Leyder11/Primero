package Main;
public class Operacion {

    public Operacion() {
    }
    public int sumar(int r, int s){
       return  r = r+s;
}
      public int restar(int r, int s){
       return  r = r-s;
}
      public int multi(int r, int s){
       return  r = r*s;
}
      public int divi(int r, int s){
       return  r = r/s;
      }
}